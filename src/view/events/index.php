    <p class="tinnenpot__text">
      Tinnenpot ligt in het hartje van het <strong class='font--opensans-bold'>Prinsenhof</strong>,
      het historisch <strong class='font--opensans-bold'>centrum</strong> van
      <strong class='font--opensans-bold'>Gent</strong>. In een vorig leven behoorde
      Tinnenpot toe aan het Arca-theater, maar toen Arca in 2001 fusioneerde met NTG tot het publiekstheater, besliste
      Tinnenpot om zelfstandig verder te gaan met Jo Decaluwe als zakelijk en artistiek leider.
    </p>
    <p class="tinnenpot__text">
      In Tinnenpot worden eigen producties gebracht, maar er is ook plaats voor
      <strong class='font--opensans-bold'>gastproducties</strong> waarbij zalen
      en personeel ter beschikking worden gesteld. Naast theater er zijn ook andere
      <strong class='font--opensans-bold'>podiumkunsten</strong> als muziek, dans, film, video.
      Door het brede aanbod is er dus voor elk wat wils.
      Zowel jong als oud kunnen terecht bij Tinnenpot.
    </p>
